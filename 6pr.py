# Ввод массива с клавиатуры
print("Введите 10 целых чисел:")
# Просим пользователя ввести числа

arr = []
# Создаем пустой список для хранения чисел

for i in range(10):
# Цикл for, который повторится 10 раз (от 0 до 9)
    
    num = int(input(f"Число {i+1}: "))
